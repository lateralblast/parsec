def get_io_desc_from_driver(io_driver)
  io_desc = ""
  case io_driver
  when /bge/
    io_desc = "Broadcom Gb Ethernet"
  when /rmc_comm/
    io_desc = "Remote Management Card"
  when /ohci/
    io_desc = "USB Controller"
  when /serial|^su$/
    io_desc = "Serial Controller"
  when /uata/
    io_desc = "Ultra ATA Controller"
  when /pmu/
    io_desc = "Power Management Unit"
  end
  return io_desc
end

def get_io_vendor_from_vendor_id(io_vendid)
  io_vendor = $pci_ids.grep(/^#{io_vendid}/)
  if io_vendor
    io_vendor = io_vendor[0].chomp.split(/\s+/)[1..-1].join(" ")
  end
  return io_vendor
end

def get_io_device_from_vendor_id(io_vendid,io_devid)
  found_vendor = 0
  $pci_ids.each do |line|
    line.chomp
    if line.match(/^#{io_vendid}/)
      found_vendor = 1
    end
    if found_vendor == 1
      if line.match(/#{io_devid}/)
        io_devid = line.split(/\s+/)[2..-1].join(" ")
      end
    end
  end
  return io_devid
end
